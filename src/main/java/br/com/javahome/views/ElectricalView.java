    private void receive(RoomData data) {
        outlets.clear();
        outlets.addAll(data.outlets());
        SwingUtilities.invokeLater(() -> {
            sensors.removeAllItems();
            outlets.forEach(it -> sensors.addItem(it.id()));
        });
    }

    private void createDataset(List<ConsumeValue> consumeValueList) {
        dataset.clear();
        consumeValueList.forEach(it -> dataset.addValue(it.value(), "", it.time()));
    }

