    private void updateLabels(final Outlet outlet) {
        name.setText(outlet.name());
        type.setText(ApplicationContext.getProperty(outlet.type().property()).toUpperCase());
        powerUsage.setText(numberFormat.format(outlet.powerUsage()) + " Kw/h");
        status.setText(ApplicationContext.getProperty(outlet.inUse() ? "app.messages.busy" : "app.messages.available"));
        createDataset(outlet.historyConsume());
    }

